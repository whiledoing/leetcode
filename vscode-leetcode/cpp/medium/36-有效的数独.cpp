#include <vector>
#include <unordered_map>
#include <unordered_set>
#include <string>
using namespace std;

// @lc code=start
class Solution {
private:
    unordered_set<string> m_s;

public:
    bool isValidSudoku(vector<vector<char>>& board) {
        for (int i = 0; i < board.size(); ++i) {
            for (int j = 0; j < board[i].size(); ++j) {
                if (board[i][j] == '.') continue;
                string c(1, board[i][j]);
                if (!insert(c+"-row-"+to_string(i)) ||
                    !insert(c+"-col-"+to_string(j)) ||
                    !insert(c+"-block-"+to_string(i/3)+"-"+to_string(j/3)))
                    return false;
            }
        }
        return true;
    }

    bool insert(string v) {
        if (m_s.find(v) != m_s.end()) return false;
        m_s.insert(v);
        return true;
    }
};
